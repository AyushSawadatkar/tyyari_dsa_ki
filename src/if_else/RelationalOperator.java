package if_else;

public class RelationalOperator {
    static void main() {
        System.out.println(5>3); //greater
        System.out.println(9<8); //lesser
        System.out.println(9>=0); //greater than and equal to
        System.out.println(5<=7); //lesser than and equal to
        System.out.println(2==2);//equal to equal
        System.out.println(4!=7);// not equal to
    }
}
